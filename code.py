import pandas as pd

# Load the dataset
data = pd.read_csv("/content/drive/MyDrive/Disease_semi-final_draft_1.csv")  # Replace "your_dataset.csv" with the actual dataset file path

# Define a function to predict diseases based on user input
def predict_disease():
    gender = input("Are you Male or Female? ").strip()
    fever = input("Do you have Fever? (Yes/No) ").strip()
    cough = input("Do you have Cough? (Yes/No) ").strip()
    fatigue = input("Do you have Fatigue? (Yes/No) ").strip()
    difficulty_breathing = input("Do you have Difficulty Breathing? (Yes/No) ").strip()
    blood_pressure = input("What is your Blood Pressure level? (Low/Normal/High) ").strip()
    cholesterol_level = input("What is your Cholesterol Level? (Low/Normal/High) ").strip()

    # Filter the dataset based on user input
    filtered_data = data[
        (data['Fever'] == fever) &
        (data['Cough'] == cough) &
        (data['Fatigue'] == fatigue) &
        (data['Difficulty Breathing'] == difficulty_breathing) &
        (data['Gender'] == gender) &
        (data['Blood Pressure'] == blood_pressure) &
        (data['Cholesterol Level'] == cholesterol_level)
    ]

    if not filtered_data.empty:
        total_matches = len(filtered_data)

        # Calculate probabilities
        disease_counts = filtered_data['Disease'].value_counts()
        probabilities = {disease: count / total_matches for disease, count in disease_counts.items()}

        print("Possible diseases you might be carrying:")
        for disease, probability in probabilities.items():
            print(f"- {disease} (Probability: {probability:.2%})")
    else:
        print("No matching diseases found based on your input.")

# Execute the prediction function
predict_disease()